/** @file HTML escaping utilities for chart text rendering */

/** Escapes HTML special characters to prevent XSS and ensure proper rendering. */
export function escapeHtml(text: string): string {
  return text
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;")
    .replace(/'/g, "&#39;");
}
